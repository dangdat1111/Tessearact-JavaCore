public class DataType {
    public static void main(String[] args) {
        int num1 = 9;
        byte by = 127;
        short sh = 558;
        long l = 5854L;

        float f = 5.8F;
        double d = 5.8;

        char c = 'k';

        boolean b = true;

        //Literals trong Java là các giá trị cố định, được mã hóa trực tiếp vào chương trình. Chúng là những đại diện trực tiếp cho một giá trị, ví dụ như số 100, ký tự 'A', hoặc chuỗi "Hello".
        // Integer Literals
        int decimalVal = 100;
        int octalVal = 0144; // 100
        int hexVal = 0x64; // 100
        long longVal = 10000000000L;

        // Floating-Point Literals
        float floatVal = 3.14f;
        double doubleVal = 1.234E5; // 123400.0

        // Character Literals
        char charVal = 'J';
        char unicodeChar = '\u004A'; // 'J'
        char newLine = '\n';

        // String Literals
        String greeting = "Hello, Java!";

        // Boolean Literals
        boolean isTrue = true;

        // Null Literal
        String emptyString = null;

    }
}
